package com.class8;

public class ForLoop {

	public static void main(String[] args) {
		//initialize    test condition    increment
		for (int i=1;       i<=5;           i++) {
			System.out.println("Good morning");
		}
	//print numbers from 1-10
		for (int a=1; a<=10; a++) {
			System.out.println(a);
		}
	}
}
