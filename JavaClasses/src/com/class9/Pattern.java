package com.class9;

public class Pattern {

	public static void main(String[] args) {

		for (int k=1; k<=5; k++) {
			
			for (int l=1; l<=5; l++) {
				
				System.out.println("*");
			}
		System.out.println("___________________");
		
		for (int i=1; i<5; i++) {
			for (int j=1; j<6; j++) {
				System.out.print(j);
			}
			System.out.println();
		}
		System.out.println("++++++++++++++++++");
		
		for (int a=6; a>=1; a--) {
			for (int b=5; b>=1; b--) {
				System.out.print(b);
			}
			System.out.println();
		}
		
		
		}
		}
}
